.global _alltraps
_alltraps:
	pushl %ds
	pushl %es
	pushal

	movw $GD_KD,%ax
	movw %ax,%ds
	movw %ax,%es

	pushl %esp /*esp指向一个Trapframe的结构体*/
	call trap
	addl $4,%esp

.globl trapret
trapret:
	addl $4,%esp /*弹出调用trapret时cpu压入的返回地址*/
	popal
	popl %es
	popl %ds
	addl $0x8,%esp   /*弹出中断号trapno和错误码err*/
#wait:	jmp wait
	iret
